package fr.distrimind.oss.upnp.common.support.model.dlna.types;

/**
 *
 * @author Mario Franco
 */
public class CodedDataBuffer {
    
    public enum TransferMechanism {
        IMMEDIATELY,
        TIMESTAMP,
        OTHER
	}
    
    private final Long size;
    private final TransferMechanism tranfer;

    public CodedDataBuffer(Long size, TransferMechanism transfer) {
        this.size = size;
        this.tranfer = transfer;
    }
    
    /**
     * @return the size
     */
    public Long getSize() {
        return size;
    }

    /**
     * @return the tranfer
     */
    public TransferMechanism getTranfer() {
        return tranfer;
    }
}
